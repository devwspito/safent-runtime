function detectLang() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored && SUPPORTED.includes(stored)) return stored;
  } catch { /* localStorage unavailable */ }
  const nav = (navigator.language || 'es').slice(0, 2).toLowerCase();
  return SUPPORTED.includes(nav) ? nav : 'es';
}

let currentLang = detectLang();
const listeners = new Set();

function interpolate(str, vars) {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (m, k) => (k in vars ? String(vars[k]) : m));
}

/** Translate a key with optional {var} interpolation. Falls back to es, then the key. */
export function t(key, vars) {
  const table = STRINGS[currentLang] ?? STRINGS.es;
  const raw = table[key] ?? STRINGS.es[key] ?? key;
  return interpolate(raw, vars);
}

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  if (!SUPPORTED.includes(lang) || lang === currentLang) return;
  currentLang = lang;
  try { localStorage.setItem(STORAGE_KEY, lang); } catch { /* ignore */ }
  document.documentElement.lang = lang;
  applyStaticI18n();
  listeners.forEach(cb => { try { cb(lang); } catch { /* a listener must not break others */ } });
}

/** Subscribe to language changes; returns an unsubscribe fn. */
export function onLangChange(cb) {
  listeners.add(cb);
  return () => listeners.delete(cb);
}

/** Localise static markup: [data-i18n] sets textContent, [data-i18n-attr] sets attributes. */
export function applyStaticI18n(root = document) {
  root.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  root.querySelectorAll('[data-i18n-attr]').forEach(el => {
    // Format: "attr:key" or "attr1:key1;attr2:key2"
    el.getAttribute('data-i18n-attr').split(';').forEach(pair => {
      const [attr, key] = pair.split(':');
      if (attr && key) el.setAttribute(attr.trim(), t(key.trim()));
    });
  });
}

